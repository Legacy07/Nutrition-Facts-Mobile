package com.turkishlegacy.nutritionfactsmobile;

public class MacrosCalculation {

    public MacrosCalculation() {
    }

    public double calories(double total, double goal) {
        double remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public double protein(double total, double goal) {
        double remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public double carb(double total, double goal) {
        double remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public double fat(double total, double goal) {
        double remaining = 0;
        remaining = goal - total;

        return remaining;
    }

}
