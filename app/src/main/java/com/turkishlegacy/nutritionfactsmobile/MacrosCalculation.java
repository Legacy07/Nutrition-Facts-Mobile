package com.turkishlegacy.nutritionfactsmobile;

public class MacrosCalculation {

    public MacrosCalculation() {
    }

    public int calories(int total, int goal) {
        int remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public int protein(int total, int goal) {
        int remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public int carb(int total, int goal) {
        int remaining = 0;
        remaining = goal - total;

        return remaining;
    }

    public int fat(int total, int goal) {
        int remaining = 0;
        remaining = goal - total;

        return remaining;
    }

}
